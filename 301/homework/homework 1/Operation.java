/**
 * Operations that can be performed on a Puzzle State.
 * @author Probir
 */
public enum Operation{
	/**
	 * Move Up operation
	 */
	MOVEUP, 
	/**
	 * Move Down Operation
	 */
	MOVEDOWN, 
	/**
	 * Move Left Operation
	 */
	MOVELEFT, 
	/**
	 * Move Right Operation
	 */
	MOVERIGHT
}
